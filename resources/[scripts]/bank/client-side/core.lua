-----------------------------------------------------------------------------------------------------------------------------------------
-- VRP
-----------------------------------------------------------------------------------------------------------------------------------------
local Tunnel = module("vrp","lib/Tunnel")
-----------------------------------------------------------------------------------------------------------------------------------------
-- CONNECTION
-----------------------------------------------------------------------------------------------------------------------------------------
vSERVER = Tunnel.getInterface("bank")
-----------------------------------------------------------------------------------------------------------------------------------------
-- LOCATION
-----------------------------------------------------------------------------------------------------------------------------------------
local Location = {
	vec3(149.64,-1041.36,29.59),
	vec3(313.95,-279.74,54.39),
	vec3(-351.2,-50.57,49.26),
	vec3(-2961.85,482.87,15.92),
	vec3(1175.09,2707.53,38.31),
	vec3(-1212.37,-331.37,38.0),
	vec3(-112.86,6470.46,31.85)
}
-----------------------------------------------------------------------------------------------------------------------------------------
-- THREADLOCATION
-----------------------------------------------------------------------------------------------------------------------------------------
CreateThread(function()
	for Number,v in pairs(Location) do
		exports.target:AddCircleZone("Bank:"..Number,v,0.1,{
			name = "Bank:"..Number,
			heading = 0.0,
			useZ = true
		},{
			Distance = 1.75,
			options = {
				{
					event = "Bank",
					label = "Abrir",
					tunnel = "client"
				}
			}
		})
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- BANK
-----------------------------------------------------------------------------------------------------------------------------------------
AddEventHandler("Bank",function()
	if not exports.hud:Wanted() then
		SetNuiFocus(true,true)
		TransitionToBlurred(1000)
		TriggerEvent("hud:Active",false)
		SendNUIMessage({ Action = "Open", Payload = { Name = LocalPlayer.state.Name, Passport = LocalPlayer.state.Passport } })
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- CLOSE
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNUICallback("Close",function(Data,Callback)
	SetNuiFocus(false,false)
	TransitionFromBlurred(1000)
	TriggerEvent("hud:Active",true)

	Callback("Ok")
end)
-----------------------------------------------------------------------------------------------------------------------------------------
-- BANK:NOTIFY
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("bank:Notify")
AddEventHandler("bank:Notify",function(Title,Message,Type)
	SendNUIMessage({ Action = "Notify", Payload = { Title = Title, Message = Message, Type = Type } })
end)
